function buildXML(iteration) {
  var xml = builder.create('root')
    .ele('xmlbuilder')
    .ele('repo', {
      'type': 'git'
    }, 'git://github.com/oozcitak/xmlbuilder-js.git')

    for (let i=0;i<(iteration-139)/44;i++) {
      var item = xml.ele('data');
       item.att('x', 1);
       item.att('y', 1 * 1);
       item.att('z', 1 * 1);
       item.att('a', 1);
       item.att('b', 1 * 1);
       item.att('c', 1 * 1);

    }

    xml = xml.end({
      pretty: true
    });
    return xml
}
